export function translateGameHtml(html: string, language: Language): string {
  if (language !== 'ja') return html
  let out = html
  for (const [zh, ja] of ZH_TO_JA) {
    if (!out.includes(zh)) continue
    out = out.split(zh).join(ja)
  }
  return out
}

